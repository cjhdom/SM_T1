
public class Message {
	private String type;
	private String image;
	private String sound;
	private String word;
	private String mean;
	private String answer;
	private String reRight;
	private String reWrong;
	private String question;
	private String resound;
	
	public Message(){
		
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	public String getResound() {
		return resound;
	}

	public void setResound(String resound) {
		this.resound = resound;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getSound() {
		return sound;
	}

	public void setSound(String sound) {
		this.sound = sound;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public String getMean() {
		return mean;
	}

	public void setMean(String mean) {
		this.mean = mean;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public String getReRight() {
		return reRight;
	}

	public void setReRight(String reRight) {
		this.reRight = reRight;
	}

	public String getReWrong() {
		return reWrong;
	}

	public void setReWrong(String reWrong) {
		this.reWrong = reWrong;
	}

	public String getQuestion() {
		return question;
	}

	public void setQuestion(String question) {
		this.question = question;
	}
	
}
